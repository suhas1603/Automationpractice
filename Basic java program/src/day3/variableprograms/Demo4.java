package day3.variableprograms;

public class Demo4 {

	public static void main(String[] args) {
		int age = 28;
		double salary = 50000;
		float var = 25000;
		System.out.println("Age = " + age);
		System.out.println("Salary = " + salary);
		System.out.println("Var = " + var);

		var = 25000.25f;
		System.out.println("Updated Var = " + var);

	}

}
