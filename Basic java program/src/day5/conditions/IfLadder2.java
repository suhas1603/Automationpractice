package day5.conditions;

public class IfLadder2 {

	public static void main(String[] args) {
		int number = -5;
		if (number > 0) {
			System.out.println("It is positive num");
		} else if (number < 0) {
			System.out.println("It is negative num");
		} else {
			System.out.println("it is zero");
		}

	}

}
