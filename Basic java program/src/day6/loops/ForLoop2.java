package day6.loops;

public class ForLoop2 {

	public static void main(String[] args) {
		System.out.println("*********Printing a to z char********");
		for (char c1 = 'a'; c1 <= 'z'; c1++) {
			System.out.print(c1 + " ");

		}

	}
}
//here we use print only and space to give us output in one line...