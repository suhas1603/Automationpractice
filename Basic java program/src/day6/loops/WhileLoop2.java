package day6.loops;

public class WhileLoop2 {

	public static void main(String[] args) {
		char ch1 = 'a';
		while (ch1 <= 'z') {
			System.out.print(ch1 + " ");
			ch1++;
		}

		System.out.println("***************reverse***************");
		char c1 = 'z';
		while (c1 >= 'a') {
			System.out.print(c1 + " ");
			c1--;

		}

	}

}
