package day6.loops;

public class WhileLoop1 {

	public static void main(String[] args) {
		int i=0;
		System.out.println("=======printing numbers 0 to 4 ========");
		/*
		 * while(condition) -----in boolean
		 * {
		 *     if true body executed
		 * }
		 */
		while(i<=4) {
			System.out.println(i);
			i++;
		}
        int a=0;
        while(a<=4) {
        	System.out.println("hello"+a);
        	a++;
        }
	}

}
